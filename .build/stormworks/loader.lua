function onTick()
	local readAddress = 0
	for i=0, 31 do
		readAddress = readAddress | (input.getBool(i+1) and 1 or 0) << i
	end
	local pack = program:sub((readAddress*4)+1, (readAddress*4)+4)
	for i=0, 3 do
		for j=0, 7 do
			output.setBool(j+1 + (i*8), pack:sub(i+1, i+1):byte() & (1 << j) > 0)
		end
	end
end